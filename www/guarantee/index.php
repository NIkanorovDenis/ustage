<h4><b>Статья 19. Сроки предъявления потребителем требований в отношении недостатков товара</b></h4>
    <br>
1. Потребитель вправе предъявить предусмотренные статьей 18 настоящего Закона требования к продавцу (изготовителю, уполномоченной организации или уполномоченному индивидуальному предпринимателю, импортеру) в отношении недостатков товара, если они обнаружены в течение гарантийного срока или срока годности.<br>
(в ред. Федерального закона от 21.12.2004 N 171-ФЗ)<br>
В отношении товаров, на которые гарантийные сроки или сроки годности не установлены, потребитель вправе предъявить указанные требования, если недостатки товаров обнаружены в разумный срок, но в пределах двух лет со дня передачи их потребителю, если более длительные сроки не установлены законом или договором.<br>
(п. 1 в ред. Федерального закона от 17.12.1999 N 212-ФЗ)<br><br>
2. Гарантийный срок товара, а также срок его службы исчисляется со дня передачи товара потребителю, если иное не предусмотрено договором. Если день передачи установить невозможно, эти сроки исчисляются со дня изготовления товара.<br>
(в ред. Федерального закона от 17.12.1999 N 212-ФЗ)<br>
Для сезонных товаров (обуви, одежды и прочих) эти сроки исчисляются с момента наступления соответствующего сезона, срок наступления которого определяется соответственно субъектами Российской Федерации исходя из климатических условий места нахождения потребителей.<br>
При продаже товаров по образцам, по почте, а также в случаях, если момент заключения договора купли-продажи и момент передачи товара потребителю не совпадают, эти сроки исчисляются со дня доставки товара потребителю. Если потребитель лишен возможности использовать товар вследствие обстоятельств, зависящих от продавца (в частности, товар нуждается в специальной установке, подключении или сборке, в нем имеются недостатки), гарантийный срок не течет до устранения продавцом таких обстоятельств. Если день доставки, установки, подключения, сборки товара, устранения зависящих от продавца обстоятельств, вследствие которых потребитель не может использовать товар по назначению, определить невозможно, эти сроки исчисляются со дня заключения договора купли-продажи.<br>
(в ред. Федерального закона от 17.12.1999 N 212-ФЗ)<br>
Срок годности товара определяется периодом, исчисляемым со дня изготовления товара, в течение которого он пригоден к использованию, или датой, до наступления которой товар пригоден к использованию.<br>
Продолжительность срока годности товара должна соответствовать обязательным требованиям к безопасности товара.
(в ред. Федерального закона от 21.12.2004 N 171-ФЗ)<br><br>
3. Гарантийные сроки могут устанавливаться на комплектующие изделия и составные части основного товара. Гарантийные сроки на комплектующие изделия и составные части исчисляются в том же порядке, что и гарантийный срок на основной товар.<br>
Гарантийные сроки на комплектующие изделия и составные части товара считаются равными гарантийному сроку на основное изделие, если иное не установлено договором. В случае, если на комплектующее изделие и составную часть товара в договоре установлен гарантийный срок меньшей продолжительности, чем гарантийный срок на основное изделие, потребитель вправе предъявить требования, связанные с недостатками комплектующего изделия и составной части товара, при их обнаружении в течение гарантийного срока на основное изделие, если иное не предусмотрено договором.<br>
(в ред. Федерального закона от 17.12.1999 N 212-ФЗ)<br>
Если на комплектующее изделие установлен гарантийный срок большей продолжительности, чем гарантийный срок на основной товар, потребитель вправе предъявить требования в отношении недостатков товара при условии, что недостатки комплектующего изделия обнаружены в течение гарантийного срока на это изделие, независимо от истечения гарантийного срока на основной товар.<br><br>
4. Сроки, указанные в настоящей статье, доводятся до сведения потребителя в информации о товаре, предоставляемой потребителю в соответствии со статьей 10 настоящего Закона.<br><br>
5. В случаях, когда предусмотренный договором гарантийный срок составляет менее двух лет и недостатки товара обнаружены потребителем по истечении гарантийного срока, но в пределах двух лет, потребитель вправе предъявить продавцу (изготовителю) требования, предусмотренные статьей 18 настоящего Закона, если докажет, что недостатки товара возникли до его передачи потребителю или по причинам, возникшим до этого момента.<br>
(п. 5 введен Федеральным законом от 17.12.1999 N 212-ФЗ)<br><br>
6. В случае выявления существенных недостатков товара потребитель вправе предъявить изготовителю (уполномоченной организации или уполномоченному индивидуальному предпринимателю, импортеру) требование о безвозмездном устранении таких недостатков, если докажет, что они возникли до передачи товара потребителю или по причинам, возникшим до этого момента. Указанное требование может быть предъявлено, если недостатки товара обнаружены по истечении двух лет со дня передачи товара потребителю, в течение установленного на товар срока службы или в течение десяти лет со дня передачи товара потребителю в случае неустановления срока службы. Если указанное требование не удовлетворено в течение двадцати дней со дня его предъявления потребителем или обнаруженный им недостаток товара является неустранимым, потребитель по своему выбору вправе предъявить изготовителю (уполномоченной организации или уполномоченному индивидуальному предпринимателю, импортеру) иные предусмотренные пунктом 3 статьи 18 настоящего Закона требования или возвратить товар изготовителю (уполномоченной организации или уполномоченному индивидуальному предпринимателю, импортеру) и потребовать возврата уплаченной денежной суммы.

